                        <li>Hard</li>
                        <li>Extreme</li>
                        <li>Suicide Mission</li>
                        <li>Impossible</li>
                        <li>Helldive</li>
                        <li>Super Helldive</li>
                    </ol>

                    <p>A core gameplay feature carried over from the first game is the use of
                        <strong>"Stratagems",</strong>
                        powerful equipment which must be called in from orbit by executing a sequence of directional
                        button
                        or arrow
                        key inputs. Players may select up to four Stratagems in advance to take into the mission with
                        them,
                        as well
                        as a "Booster" which affects the whole team. Helldivers 2 features cooperative multiplayer with
                        up
                        to four
                        players,<em> but does not include a dedicated single-player campaign</em>. It also provides
                        optional
                        cross-play between PlayStation 5 and Windows. Friendly fire is always on. In the initial days of
                        release,
                        gameplay involved staff at the studio observing players and influencing gameplay in real time.
                    </p>

                    <img src="images/helldivers/helldivers2.3.jpg" height=300 width=300
                        alt="Helldivers 2 Stratagems list">
                    <div>Helldivers 2 Stratagems list</div>

                    <p>The game features live service content updates and has a battle pass system called
                        <strong>"Warbonds"
                            that
                            never expire</strong>. Warbonds can be purchased with in-game currency known as <em>"Super
                            Credits"</em>,
                        which players can obtain in-game, purchase via microtransactions, or unlock as part of the
                        Warbonds
                        themselves. As of <strong>6 February 2025</strong>, eleven Warbonds have been released:
                        <strong>"Helldivers
                            Mobilize"; "Steeled Veterans"; "Cutting Edge"; "Democratic Detonation"; "Polar Patriots";
                            "Viper
                            Commandos"; "Freedom's Flame"; "Chemical Agents"; "Truth Enforcers"; "Urban Legends"; and
                            "Servants of
                            Freedom"</strong>. Players can also obtain additional in-game currency in the form of
                        <em>"Warbond
                            Medals" and "Requisition Slips"</em>, which may be discovered during missions or awarded by
                        completing
                        operations, missions, and objectives.
                    </p>

                    <p>As players gain experience and progress in levels, they unlock different <em>weapons and
                            Stratagems</em> to
                        use during missions. Players can also collect <strong>"Samples"</strong>, which are used to
                        upgrade
                        the
                        player's Super Destroyer starship, providing advantages during gameplay. By completing
                        Operations,
                        players
                        also contribute to the collective progress of <strong>"Major Orders"</strong>, which are
                        mass-collaborative
                        missions that directly affect the outcome of the Galactic War in-game, if a major order is
                        completed
                        each
                        player is given a reward.</p>
