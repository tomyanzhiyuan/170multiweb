                    <h2>Synopsis</h2>

                    <h3>Setting</h3>
                    <p>Helldivers 2 takes place in the <strong>22nd century</strong>, one hundred years after the
                        triumph of
                        Super
                        Earth, a self-described managed democracy, over the Bugs, Cyborgs, and Illuminate during the
                        First
                        Galactic
                        War. Humanity seized <em>faster-than-light (FTL) travel technology</em> from the defeated
                        Illuminate, and
                        used it to conquer the galaxy during a period known as the <strong>Great
                            Democratization</strong>.
                        Humanity
                        also discovered that the decomposing corpses of the Bugs contain a unique and highly valuable
                        resource known
                        as <strong>E-710</strong> which is used for FTL travel. Bug farms are established on
                        human-colonized
                        worlds,
                        and after decades of genetic engineering and accelerated evolution, they become known as the
                        <strong>Terminids</strong>.
                    </p>

                    <h3>Plot</h3>
                    <p>The events of Helldivers 2 begins after the Super Earth Armed Forces fail to contain the
                        Terminids,
                        <em>which have escaped their farms and are causing widespread chaos and destruction</em>.
                        Simultaneously, a
                        new threat emerges in the form of the <strong>Automatons</strong>, descendants of the defeated
                        Cyborgs,
                        intent on getting revenge against humanity. Subsequently, the <strong>Illuminate</strong>, an
                        ancient and
                        technologically advanced alien race thought to be extinct, return and invade Super Earth's
                        colonies.
                        In
                        response, Super Earth declares war and mobilizes the Helldivers, an elite task force, to combat
                        the
                        threats.
                    </p>

