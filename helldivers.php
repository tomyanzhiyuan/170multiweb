                <section>
                    <h2>Gameplay</h2>

                    <p>Helldivers 2 is a shooter game primarily played from a third-person perspective. Players complete
                        tasks
                        <em>(called "Missions") in procedurally generated levels (or "Operations")</em> while fending
                        off
                        hordes of
                        enemies. Players choose the difficulty level of their Operations, ranging from level 1
                        ("Trivial")
                        to level
                        10 ("Super Helldive").
                    </p>

                    <h3>Difficulty Level of Operations from Easy to Hardest</h3>
                    <ol>
                        <li>Trivial</li>
                        <li>Easy</li>
                        <li>Medium</li>
