                    <h2>Development and Release</h2>
                    <p>On <strong>3 December 2020, Arrowhead Game Studios</strong> revealed that work had started on a
                        new
                        project
                        for PlayStation 5. It was confirmed that the game would be a third-person shooter. Like the
                        first
                        game,
                        Helldivers 2 runs on the discontinued Autodesk Stingray game engine (originally known as
                        Bitsquid).
                        Arrowhead Game Studios founder <strong>Johan Pilestedt</strong> confirmed on social media that
                        "The
                        project
                        started before [Stingray] was discontinued," adding, "Our crazy engineers had to do everything,
                        with
                        no
                        support to build the game to parity with other engines. On <strong>13 September 2021</strong>,
                        Helldivers 2
                        was mentioned in a <em>GeForce Now</em> leak. The game was teased in a TikTok post showing
                        Arrowhead's
                        social media manager starting their workday, which is followed by a series of posts from fans
                        demanding them
                        to release the game. On <strong>18 August 2022</strong>, a 12-second clip of gameplay was leaked
                        on
                        Twitter,
                        but was later removed. On <strong>24 May 2023</strong>, Helldivers 2 was announced during the
                        <em>2023
                            PlayStation Showcase</em>, with a slated release of sometime in 2023. <strong>On 6 July
                            2023</strong>, a
                        trailer showcasing gameplay was released. On <strong>30 July 2023</strong>, the game received an
                        M
                        rating by
                        the Entertainment Software Rating Board for "blood and gore" and "intense violence". <strong>On
                            14
                            September
                            2023</strong>, gameplay was shown in a PlayStation State of Play event, and it was announced
                        that the
                        game would release on <strong>8 February 2024</strong>. Pre-orders became available on
                        <strong>22
                            September
                            2023</strong>. <strong>On 8 February 2024</strong>, the game was released on the PlayStation
                        Store and
                        Steam.
                    </p>

