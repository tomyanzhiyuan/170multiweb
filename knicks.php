            <h3><em><u>The Early Days Knickerbockers</u></em></h3>
            <p>The team, established by Ned Irish in 1946, was one of the founding members of the Basketball Association
                of
                America (BAA), which became the NBA after merging with the rival National Basketball League (NBL) in
                1949.
                The Knicks were successful during their early years and were constant playoff contenders under the
                franchise's first head coach, Joe Lapchick. Beginning in 1950, the Knicks made three consecutive
                appearances
                in the NBA Finals, all of which were losing efforts. Lapchick resigned in 1956 and the team subsequently
                began to falter.

                It was not until the late 1960s when Red Holzman became the head coach that the Knicks began to regain
                their
                former dominance. Holzman successfully guided the Knicks to two NBA championships, in 1970 and 1973. The
                Knicks of the 1980s had mixed success that included six playoff appearances; however, they failed to
                participate in the NBA Finals.

                The playoff-level Knicks of the 1990s were led by future Hall of Fame center Patrick Ewing; this era was
                marked by passionate rivalries with the Chicago Bulls, Indiana Pacers, and Miami Heat. During this time,
                they were known for playing tough defense under head coaches Pat Riley and Jeff Van Gundy, making NBA
                Finals
                appearances in 1994 and 1999. However, they were unable to win an NBA championship during this era.</p>
