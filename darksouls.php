                    <h2>Bosses</h2>
                    <hr>
                    <br>
                    <h3>The Witch of Izalith</h3>
                    <img src="images/darksouls/witchizalith.jpg" width="180" height="178"
                        alt="A picture of the Witch of Izalith before she created the chaos flame and turned herself into the Bed of Chaos">
                    <p>One of the four original Lord Soul bearers, her Lord Soul granted the power of fire. She is a
                        witch
                        and master of the now forgotten fire sorceries, and founded the city of Izalith with her
                        daughters
                        deep underground. When the First Flame began to fade, she attempted to create a second First
                        Flame
                        to replace it using her witchcraft and a special soul, but failed catastrophically and instead
                        created the Flame of Chaos. The birth of Chaos marked the birth of demons, and its flame
                        transformed
                        the Witch of Izalith and all her daughters into demons barring Quelana, who escaped the
                        destruction
                        of her home. As the daughters of Izalith were the only practitioners of fire sorcery, it is now
                        a
                        forgotten art, but the creation of Chaos also created pyromancy as a byproduct. Gwyn waged war
                        against the demons and eventually routed them, shackling the Bed of Chaos, their source of life,
                        to
                        contain them.</p>

                    <h3>Ornstein and Smough</h3>
                    <img src="images/darksouls/ornsteinandsmough.jpg" width="180" height="178"
                        alt="A picture of Dragon Knight Ornstein and Executioner Smough during their intense boss fight">
                    <p>Two knights of Lord Gwyn, they are tasked with guarding Gwynevere.</p>

                    <h3>Pinwheel</h3>
                    <img src="images/darksouls/pinwheel.jpg" width="180" height="178"
                        alt="A picture of the necromancer Pinwheel during his introductory cutscene">
                    <p>Necromancer who stole the Right of Kindling and fled to the Catacombs. Pinwheel has three faces:
                        Father, Child, and Mother. Pinwheel was once a man who lost his son and wife. He attempted to
                        bring
                        them back through necromancy, but ended up fusing their bodies with his. When Pinwheel clones
                        himself, it is actually his attempt at unfusing the three bodies. Throughout the Pinwheel boss
                        fight, the masks can be heard whispering in the background of the music. What the Mother and
                        Child
                        say is difficult to make out. The Mother chants something along the lines of, "We will never
                        die,"
                        which refers to how the player will always obtain one of the Family Masks. It is believed that
                        the
                        Child chants, "Help me." Although it is difficult to hear the Mother and Child, it is easy to
                        hear
                        what the Father says. "I'm sorry. I'm so, so sorry."</p>

                    <h3>Taurus Demon</h3>
                    <img src="images/darksouls/taurusdemon.png" width="180" height="178"
                        alt="A picture of the Taurus Demon encountered on the wall in the Undead Burg as the Chosen Undead readies themselves">
                    <p>A bull demon who managed to escape to the surface when Lost Izalith came into being.</p>

                    <h3>Four Kings</h3>
                    <img src="images/darksouls/fourkings.png" width="180" height="178"
                        alt="A picture of one of the Four Kings during their boss battle">
                    <p>The wraith-like remnants of the four leaders of New Londo Ruins who fell to dark after being
                        tempted
                        with the art of lifedrain. Defeating the Four Kings is necessary to the plot, and is also needed
                        for
                        those players wishing to join the Darkwraith covenant as they must defeat the Four Kings before
                        obtaining the Lordvessel from Anor Londo and talking to Kingseeker Frampt. You can also obtain
                        the
                        Lordvessel then go to kill the Four Kings instead of talking to Frampt. In order to safely
                        survive
                        the Abyss, all players (host and any summons) must equip the Covenant of Artorias Ring prior to
                        entering, and keep it on during the entire fight. Removing it at any point will result in death.
                        It
                        is not necessary to continue wearing the ring after the Four Kings are defeated.</p>
                </section>

