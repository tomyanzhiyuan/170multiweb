                    <h2>Lore</h2>
                    <hr>
                    <p>The world of Dark Souls is a world of cycles. Kingdoms rise and fall, ages come and go, and even
                        time
                        can end and restart as the flame fades and is renewed. These cycles are linked to the
                        <strong>First
                            Flame</strong>, a mysterious manifestation of life that divides and defines separate states
                        such
                        as heat and cold, or life and death. As the First Flame fades, these differences also begin to
                        fade,
                        such as life and death having little distinction, and humans becoming Undead. The onset of an
                        Age of
                        Dark, the time when the First Flame has fully died, is marked by endless nights, rampant
                        undeath,
                        time, space, and reality breaking down, lands collapsing and converging on one another, people
                        mutating into monsters, darkness covering the world, and the Gods losing their power. To avoid
                        this
                        and prolong the Age of Fire, the bearer of a powerful soul must "link" themselves to the First
                        Flame, becoming the fuel for another age. If this is not done, the First Flame will eventually
                        die,
                        and an Age of Dark will begin.</p>
                    <p>The powerful <strong>Lord Souls</strong> were taken from the First Flame, used to defeat the
                        dragons,
                        and then to establish kingdoms. Souls are inextricably (and inexplicably) linked to fire.
                        <em>Souls
                            are life, and life is fire</em>; it stands to reason that souls are fire, as well. Without
                        the
                        First Flame and without souls, there is no life. The bearer of a strong soul, called a Lord, who
                        links themselves to the First Flame, is thus rekindling the flames with their own soul,
                        returning
                        life to it. In the end, one could expect that all souls will have been returned to the First
                        Flame,
                        and the Age of Fire will have effectively ended anyways.</p>
                    <p>It is inevitable that people try to find a way around this, another way to continue the Age of
                        Fire
                        without making sacrifices, but in every case they fail. In the first Age of Fire, the Witch of
                        Izalith attempted to create a duplicate of the First Flame using her witchcraft and a special
                        soul,
                        but failed catastrophically. Instead of making another First Flame, she had created a twisted
                        Flame
                        of Chaos that produced distortions of life, turning herself and all her daughters into demons.
                        These
                        demons were malevolent, and wandered the land. Gwyn gathered up his armies and fought the demons
                        of
                        Chaos, eventually driving them back and shackling the Bed of Chaos to bind them. Knowing that
                        the
                        Age of Fire was nearing its end, under the guidance of the primordial serpent Kingseeker Frampt,
                        Gwyn offered himself to the First Flame to stave off the Dark. As the flames surged with new
                        life,
                        the knights who followed him in were burnt by the flames and now wander the world as the Black
                        Knights, hollow shells of armor who attack indiscriminately.</p>
                    <p>Before he left to link the First Flame, Gwyn gave fragments of his soul to his followers and
                        allies.
                        Ornstein, Gough, Ciaran, and Artorias, his four strongest knights who fought against the
                        dragons,
                        Seath the Paledrake, who betrayed the dragons and played a crucial role in the war, and was also
                        granted the title of Duke and permission to build a grand archive to conduct his research, and
                        the
                        Four Kings of New Londo, who ruled a city of Undead. In time, another primordial serpent
                        convinced
                        the people of the human kingdom of Oolacile to delve into the darkness of the Abyss to unearth
                        the
                        grave of an ancient human. Whether through their actions or some other means, the primordial
                        human -
                        Manus - was resurrected and driven to madness, and his wild Humanity caused the people of
                        Oolacile
                        to mutate into abominations. Manus, Father of the Abyss, began to rapidly spread Dark and
                        accelerate
                        the growth of the Abyss, and would've started an early Age of Dark had he not been defeated.
                        Even
                        so, the kingdom of Oolacile was lost to the Abyss, and eventually forgotten in the modern day.
                        Nothing remains in the present day to mark the passing of this land save for Dusk, the princess
                        of
                        Oolacile.</p>
                    <p>Gwyn's youngest son Gwyndolin, together with Kingseeker Frampt, shepherds the humans by Gwyn's
                        command. Together they built giant asylums to house the countless undead, hoping against hope
                        that
                        one of them would be chosen to defeat the Asylum Demon, and fulfill their destiny. The Undead
                        are
                        sent on a quest to ring the Bells of Awakening, collect the Lordvessel, slay the bearers of
                        Gwyn's
                        soul, then offer those souls to the Lordvessel to open the path to the Kiln of the First Flame.
                        In
                        doing so, the unnatural Undead are given true death and the living can enjoy another age of fire
                        for
                        a thousand years, while the Undead linked to the First Flame will continue to burn, alive and
                        aware,
                        until someone else comes to take their place. And thus, the cycle continues.</p>
                </section>

